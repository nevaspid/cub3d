#include "cub3d.h"

int	check_filename(char *file)
{
	int	i;

	i = -1;
	while (file[++i] != '\0')
	{
		if (file[i] == '.')
		{
			if (file[i + 1] == 'c' && file[i + 2] == 'u' && file[i + 3] == 'b'
				&& file[i + 4] == '\0')
				return (1);
		}
	}
	return (0);
}
